/**
 * Получить вопросы по сложности
 */
function getQuestionsByDifficulty(difficulty, count) {
  const pool = questionsDatabase[difficulty] || [];
  // Перемешиваем вопросы
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  
  // Если запрашиваем больше вопросов, чем есть, повторяем вопросы
  const result = [];
  while (result.length < count) {
    const needed = count - result.length;
    const toAdd = shuffled.slice(0, Math.min(needed, shuffled.length));
    result.push(...toAdd);
  }
  
  return result;
}

